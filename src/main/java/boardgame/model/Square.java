package boardgame.model;

/**
 * The Square enum represents the possible states of a square on the game board.
 * Each square can be in one of the following states: NONE, RED, YELLOW, GREEN.
 */
public enum Square {

    /**
     * Indicates there is no stone in the square on the board.
     */
    NONE,

    /**
     * Indicates there is a red stone in the square on the board.
     */
    RED,

    /**
     * Indicates there is a yellow stone in the square on the board.
     */
    YELLOW,

    /**
     * Indicates there is a green stone in the square on the board.
     */
    GREEN
}
